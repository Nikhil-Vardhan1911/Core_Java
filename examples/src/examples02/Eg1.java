package examples02;

public class Eg1 {
	
	public static void main(String[] args) {
		
		int a = 10;
		int b = 39;
	
		
		//Bitwise And operator
		System.out.println(a&b); // 2
		
		// Bitwise or operation..
		System.out.println(a|b); // 47
		
		// bitwise exclusive or operation..
		System.out.println(a^b);    //45	
	}
}
